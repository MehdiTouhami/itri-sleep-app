class SleepNight {
  final String date;
  final String sleepDuration;
  final int sleepScore;
  final String quality;
  final String deepSleep;
  final String lightSleep;
  final String remSleep;
  final String awakeTime;
  final String restingHR;
  final String hrv;
  final String stressAvg;
  final String bodyBattery;
  final String respiration;

  const SleepNight({
